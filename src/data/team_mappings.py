ALIAS_MAP = {
    "NFL": NFL_ALIASES,
    "NBA": NBA_ALIASES,
    "CFB": CFB_ALIASES,
    "MLB": MLB_ALIASES,
    "EPL": EPL_ALIASES,
    "LALIGA": LALIGA_ALIASES,
    "BUNDESLIGA": BUNDESLIGA_ALIASES,
    "SERIEA": SERIEA_ALIASES,
    "LIGUE1": LIGUE1_ALIASES,
}


def normalize_team_code(league: str, name: str | None) -> str:
    if not name:
        return ""
    cleaned = name.strip()
    if not cleaned:
        return ""
    if len(cleaned) <= 3 and cleaned.isalpha():
        return cleaned.upper()

    aliases = ALIAS_MAP.get(league.upper()) or {}
    key = cleaned.lower()
    if key in aliases:
        return aliases[key]

    # remove punctuation and try again
    simplified = key.replace(".", "").replace("-", " ")
    simplified = " ".join(simplified.split())
    if simplified in aliases:
        return aliases[simplified]

    # try splitting words and taking first three letters
    words = simplified.split()
    if len(words) == 2 and words[1] in {"fc", "sc"}:
        words = words[:1]
    if len(words) >= 2:
        candidate = (words[0][:1] + words[-1][:2]).upper()
        if len(candidate) == 3:
            return candidate

    return cleaned.upper()
