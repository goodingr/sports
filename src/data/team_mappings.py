def normalize_team_code(league: str, name: str | None) -> str:
    if not name:
        return ""
    cleaned = name.strip()
    if not cleaned:
        return ""
    if len(cleaned) <= 3 and cleaned.isalpha():
        return cleaned.upper()

    aliases = ALIAS_MAP.get(league.upper()) or {}
    key = cleaned.lower()
    if key in aliases:
        return aliases[key]

    # remove punctuation and try again
    simplified = key.replace(".", "").replace("-", " ")
    simplified = " ".join(simplified.split())
    if simplified in aliases:
        return aliases[simplified]

    canonical = _canonicalize_alias(key)
    if canonical in aliases:
        return aliases[canonical]

    # try splitting words and taking first three letters
    words = simplified.split()
    if len(words) == 2 and words[1] in {"fc", "sc"}:
        words = words[:1]
    if len(words) >= 2:
        candidate = (words[0][:1] + words[-1][:2]).upper()
        if len(candidate) == 3:
            return candidate

    return cleaned.upper()


def get_ncaab_team_code(team_id: int | str | None) -> str:
    if team_id is None:
        return ""
    if isinstance(team_id, str):
        key = team_id.strip()
    else:
        key = str(int(team_id))
    if not key:
        return ""
    if key in NCAAB_TEAM_CODES:
        return NCAAB_TEAM_CODES[key]
    if key.isdigit():
        normalized_key = str(int(key))
        code = NCAAB_TEAM_CODES.get(normalized_key)
        if code:
            return code
        return f"NCAAB{int(normalized_key):04d}"
    return normalize_team_code("NCAAB", key)


